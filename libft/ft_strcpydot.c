char	*ft_strcpydot(char *dst, const char *src, char c)
{
	int i;

	i = 0;
	while (*(src + i) != '\0' && *(src + i) != c)
	{
		*(dst + i) = *(src + i);
		i++;
	}
	*(dst + i) = '\0';
	return (dst);
}
